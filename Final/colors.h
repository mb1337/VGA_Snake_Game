#ifndef COLORS_H_
#define COLORS_H_

#define BLACK  0x00
#define D_GRAY 0x15
#define L_GRAY 0x2A
#define RED    0x03
#define GREEN  0x0C
#define YELLOW (RED|GREEN)
#define BLUE   0x30
#define NAVY   0x10
#define VIOLET (BLUE|RED)
#define CYAN   (BLUE|GREEN)
#define WHITE  0x3C


#endif /* COLORS_H_ */
